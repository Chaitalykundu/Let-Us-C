/* The case of the The value of variable and the next cases will be printed */

#include<stdio.h>
int main()
{
    int i=2;

    switch(i)
    {
        case 1:
            printf("\nI'm Case 1");
        case 2:
            printf("\nI'm Case 2");
        case 3:
            printf("\nI'm Case 3");
        default:
            printf("\nI'm in default");

    }
}
